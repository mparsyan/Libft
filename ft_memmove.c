#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t	i;
	int		pos;

	i = 0;
	if (src == NULL && dst == NULL)
		return (dst);
	if (dst < src)
	{
		while (i < len)
		{
			((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
			i++;
		}
	}
	else
	{
		while (i < len)
		{
			pos = len - 1 - i;
			((unsigned char *)dst)[pos] = ((unsigned char *)src)[pos];
			i++;
		}
	}
	return (dst);
}
